def game_loop(args):
    pygame.init()
    pygame.font.init()
    world = None

    try:
        client = carla.Client(args.host, args.port)
        client.set_timeout(2.0)

        display = pygame.display.set_mode(
            (args.width, args.height),
            pygame.HWSURFACE | pygame.DOUBLEBUF)

        world = World(client.get_world())

        # Blueprint for NPC
        blueprint = world._get_random_blueprint()

        clock = pygame.time.Clock()
        while True:
            clock.tick_busy_loop(60)
            world.render(display)
            pygame.display.flip()

            # Trying to spawn NPC if not already present
            if world.npc is None:
                transform = world.vehicle.get_transform()
                heading = transform.rotation.yaw
                gap = 10 # meters ahead
                offsetx, offsety = math.cos(heading), math.sin(heading)
                if abs(offsetx) > abs(offsety):
                    transform.location.x += gap
                else:
                    transform.location.y += gap
                world.npc = world.world.try_spawn_actor(blueprint, transform)
                print(offsetx, offsety)
                if world.npc is not None:
                    print("Spawned NPC")
                    world.npc.set_autopilot()

    finally:

        if world is not None:
            world.destroy()

        pygame.quit()


